function formatTime() {
  const totalMinutes = Number(prompt("Введіть кількість хвилин:"));
  const hours = String(totalMinutes / 60).padStart(2, "0");
  const minutes = String(totalMinutes % 60).padStart(2, "0");
  console.log(`${hours}:${minutes}`);
}
formatTime();
